    private void cargarDatos(int id_usuario) {
        
        this.usuarioEditable = control.traerUsuario(id_usuario);
        
        txtNombre.setText(usuarioEditable.getNombre());
        txtPassword.setText(usuarioEditable.getContrasenia());
        
        //Agregar todos los roles de la BD a una lista
        List<Rol> listaRoles = control.traerRoles();
        //Si la lista de roles NO esta vacia, por cada rol de la lista lo cargamos en el combo.
        if(listaRoles!=null){
            for(Rol rol : listaRoles){
            cmbRol.addItem(rol.getNombreRol());
            }
        }
        
        //Guardamos el nombre del rol que corresponde al usuario a editar.
        String rol = usuarioEditable.getRol().getNombreRol();
        
        //Guardamos el numero de roles existentes en la base de datos en una variable
        int cantidadItems = cmbRol.getItemCount();
              
        //Por cada rol que tengamos lo recorremos hasta tener el que corresponda al usuario a editar
        for(int i = 0; i<cantidadItems;i++){
            if(String.valueOf(cmbRol.getItemAt(i)).equals(rol)){
                cmbRol.setSelectedIndex(i);
            }
        }
    }
